<h1>Accessibility</h1>

<p>We work hard to ensure our website is fully accessible to all users. We constantly update for accessibility as new technologies are developed.
If you come across any part of the site that provides an accessibility problem, please let us know. You can email Mike McGrath at
<a href="mailto:mmcgrath@mcgrathsolutions.org">mmcgrath@mcgrathsolutions.org</a> or call 865-288-0912. On this page we talk about some of the techniques we used 
for accessibility.</p>

<h2>Drop-Down Navigation</h2>
<p>The drop-down menu was created using YUI libraries MenuNav node plug-in. As stated on their website 
    the MenuNav Node Plugin was built with both accessibility and usability in mind. The MenuNav Node Plugin 
    implements established mouse and keyboard interaction patterns to deliver a user experience that is both 
    familiar and easy to use. To that foundation the MenuNav Node plugin adds support for screen readers through 
    the use of the WAI-ARIA Roles and States.</p>
<p>To navigate through the drop-down menu using only a keyboard, simply tab to the menu then use your arrow keys to traverse the menu.</p>

<h2>Slide Show</h2>
<p>The slide show on the home page can be paused by hovering over the slide show with your mouse.</p>
<p>For keyboard users, simply use the left and right arrow keys to advance the slides. When you use the arrow keys to advance the slides, they will also pause. 
    For example, if you press the right arrow key, you will move one slide to the right, and the slide show will pause.</p>

<h2>Divide Content Using Headers</h2>
<p>To help screen reader users navigate the website, all content is divided using headers. Main content is contained under Heading 1 while sub content would 
    be listed under Heading 2 or 3.</p>

<h2>Resize Browser</h2>
<p>Instead of creating zoom in and out buttons, we have decided to create a short tutorial to teach users how to use the zoom features of 
    their web browser to resize the content on the pages.</p>

<h3>From the Keyboard</h3>
<p>(works in Firefox, Internet Explorer, Safari, and Google Chrome)</p>
    <ol>
        <li>To zoom in, increasing the size, press and hold Ctrl while pressing "+" (plus).</li>
        <li>To zoom out, decreasing the size, press and hold Ctrl while pressing "-" (minus).</li>
        <li>To reset size back to normal, press and hold Ctrl while pressing "0" (zero).</li>
    </ol>

<h3>From the Mouse</h3>
<p>(works in Firefox, Internet Explorer, Safari, and Google Chrome)</p>
<p>If your mouse has a scroll wheel, press and hold Ctrl while scrolling up to zoom in and down to zoom out.</p>

<h3>Firefox - Menu</h3>
<p>Press Alt V, then go to Zoom. Select either Zoom In, Zoom Out, or Reset for their corresponding action.</p>

<h3>Firefox - Toolbar</h3>
    <ol>
        <li>Right-click on an empty section of the Tab Strip, select Customize; and the Customize Toolbar window will open.</li>
        <li>In the Customize Toolbar window, click and drag the Zoom Controls toolbar item to the desired place on one of your toolbars.</li>
        <li>Click Done to save the changes to your toolbars.</li>
        <li>You can now use the + (plus) icon on the customized toolbar to zoom in, and the - (minus) icon on the customized toolbar to zoom out.</li>
    </ol>

<h3>Internet Explorer</h3>
<p>Open the Tools menu and select Zoom. Press and hold Alt while pressing X to open the Tools menu.</p>

<h3>Safari - Menu</h3>
<p>At the top of the Safari window click the View menu (looks like a little page icon left of the search bar), then go to Zoom and Zoom In, Zoom Out, 
    or Actual Size for their corresponding action.</p>

<h3>Safari - Toolbar</h3>
    <ol>
        <li>At the top of the Safari window click the Settings menu (looks like a little gear icon left of the search bar), select Customize Toolbar and the Customize Toolbar window will open.</li>
        <li>In the Customize Toolbar window, click and drag the Zoom toolbar item (contains a small A and big A) to the desired place on one of your toolbars.</li>
        <li>Click Done to save the changes to your toolbars.</li>
        <li>You can now use the big A icon on the customized toolbar to zoom in, and the small A icon on the customized toolbar to zoom out.</li>
    </ol>

<h3>Google Chrome</h3>
<p>Click the customize and control Google Chrome button (icon with 3 horizontal lines) and select the Zoom option (+ (plus) for zoom in and - (minus) for zoom out).</p>