<div class="qodd">
<h2 id="fourteen">What is an associate?</h2>
<p>We refer to our interns as associates.</p>
</div>

<div class="qeven">
<h2 id="fifteen">What is the difference between an associate and a volunteer?</h2>
<p>Associates are people with disabilities that work on research and development projects as well as providing IT services. They may be paid or unpaid.
Volunteers are often used to promote the organization and to help with fundraising and with any other needs we may have. Volunteers do not need to necessarily 
have a disability and are unpaid. Both associates and volunteers may receive employment services as a benefit.</p>
</div>

<div class="qodd">
<h2 id="one">What types of positions are you looking to fill?</h2>
<p>Mostly internships, both part-time and full-time. We are always looking for volunteers, especially for projects involving small nonprofit organizations with little to no budgets. In the future, we hope to offer permanent positions when we receive bigger contracts and when more funding becomes available.</p>
</div>

<div class="qeven">
<h2 id="two">What skills are you looking for?</h2>
<p>We are looking for all sorts of skill sets revolving around information technology and computers including, but not limited to, web designers, programmers, graphic artists, database administrators, technical writers, software and web user interface testers, help desk, technical support, mechanical engineers, and electrical engineers.</p>
</div>

<div class="qodd">
<h2 id="three">How long do internships last?</h2>
<p>It depends on the projects available. Most internships last 6 months to 1 year or 240 hours.</p>
</div>

<div class="qeven">
<h2 id="four">Are internships paid?</h2>
<p>It depends on the projects available and funding. In most cases, the internships will include some stipend or hourly pay rate.</p>
</div>

<div class="qodd">
<h2 id="five">Are associates evaluated on their performance?</h2>
<p>Yes, they will be evaluated during and after their internship by their supervisor and the customer.</p>
</div>

<div class="qeven">
<h2 id="six">Is there a minimum age or grade level required?</h2>
<p>We are looking to work with everyone from high school students to Ph.D. students, and seasoned professionals.</p>
</div>

<div class="qodd">
<h2 id="seven">Do you only hire people with disabilities?</h2>
<p>Our mission is to give people with disabilities an opportunity to gain work experience. People with disabilities have a much higher unemployment rate than those without disabilities. When our clients come to us, they want to provide work opportunities to people with disabilities; therefore, they expect the people working on their projects to be people with disabilities.</p>
</div>

<div class="qeven">
<h2 id="eight">Where are the positions or internships located?</h2>
<p>All positions are remote work from home positions. All interns must be willing to use online collaboration tools. Interns are expected to keep in constant contact with supervisors and clients.</p>
</div>

<div class="qodd">
<h2 id="nine">Do you provide a computer and Internet access?</h2>
<p>No. Each associate is expected to have their own computer and Internet access. Depending on the project, software may or may not be provided.</p>
</div>

<div class="qeven">
