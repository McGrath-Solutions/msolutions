<h2 id="seven">What are the benefits of hiring people with disabilities?</h2>
<p>Sure there are hiring requirements and tax incentives, but there many more reasons for hiring people with disabilities. We want to work and do 
    not take anything for granted. We are loyal to our bosses and companies and do not jump ship when things get tough. We can adapt to most 
    situations and tend to do more work than people without disabilities. We are motivators. People with disabilities work because it gives 
    us freedom to own a home, buy a car, get married, go on vacation, and buy gifts for family and friends. These are all things that most people with 
    disabilities do not get to experience because we are not given a chance to work.</p>
</div>

<div class="qeven">
<h2 id="eight">What are the benefits of remote work?</h2>
<p>Remote work reduces company overhead because less office space is needed and electricity costs are reduced. Travel costs are almost non-existent as meetings can be 
    held online. Also employees save money on gas with no commute. Productivity also increases because employees are always near their work. If they are 
    approaching a deadline, they can work late and on weekends without having to run to the office. However, not all jobs are appropriate for remote work 
    and not all people are cut out for remote work.</p>
</div>