<h1>About Us</h1>

<div id="col1">
    
    <p>McGrath Solutions was officially incorporated as a non-profit in the State of Tennessee in August 2013. We are an IRS 501(c)(3) tax exempt organization.</p>

    <p>The founder, Mike McGrath, has been providing volunteer information technology services to nonprofit organizations since 2003 when he started making modifications to 
        the DateAble member database.</p>

    <p>Mike, who has Duchenne Muscular Dystrophy, worked remotely from home for several years for Computer Sciences Corporation as a programmer analyst before he was 
        laid off. The remote work opportunity made it possible for Mike to hold competitive employment which in turn gave him more independence due to his salary. With remote 
        work, he could work from the comfort of his home and not worry about transportation and caregiver issues. However, remote work is not for everyone. 
        At McGrath Solutions, interns and volunteers are given the chance to try working remotely to see if it is a fit for them.</p>

    <p>Having worked on several websites and databases for nonprofits and small businesses, Mike realized most of the small organizations are only run by a handful of 
        people with very little technical knowledge and budgets of nearly nothing. They are missing out on the benefits of technology or spending too much time trying to 
        learn the technology and becoming frustrated. Through McGrath Solutions, Mike hopes to remove the burdens and cost of technology so the organization can focus on helping 
        their clients and customers.</p>
    
