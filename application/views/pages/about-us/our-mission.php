<h1>Our Mission</h1>
<p>Our mission is to increase the employability of individuals with disabilities interested in science, technology, engineering, and mathematics (STEM) fields through internships and community outreach.</p>

<h1>Our Vision</h1>
<p>Our vision is to increase employment for individuals with disabilities.</p>

<h1>Our Values</h1>
<p>McGrath Solutions operates on the fact that anyone with a disability who wants to work can work. We never turn someone away because they 
    are "too" disabled. We always provide high quality, low cost information technology services making sure the customer is 100% satisfied.</p>
<p>McGrath Solutions treats all people with dignity and respect and provides a system of open communication. We promote education, training, 
    mentoring, and peer support. We are accessible and accommodating of all impairments.</p>
