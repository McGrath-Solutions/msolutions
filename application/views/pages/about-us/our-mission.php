<h1>Our Mission</h1>
<p>Our mission is to provide remote work experience to people with disabilities while providing low cost information technology services to 
    nonprofit organizations and small businesses and to provide research and development of accessibility products.</p>

<h1>Our Vision</h1>
<p>Our vision is that through the proper training, education, and work experience there will be an increase in the number of people with 
    disabilities working in science, technology, engineering, and mathematics (STEM) positions.</p>

<h1>Our Values</h1>
<p>McGrath Solutions operates on the fact that anyone with a disability who wants to work can work. We never turn someone away because they 
    are "too" disabled. We always provide high quality, low cost information technology services making sure the customer is 100% satisfied.</p>
<p>McGrath Solutions treats all people with dignity and respect and provides a system of open communication. We promote education, training, 
    mentoring, and peer support. We are accessible and accommodating of all impairments.</p>