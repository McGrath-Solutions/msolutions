<p>We refer to our interns as associates. We feel this is a more professional title for our interns because of the high skill involved with our challenging internships.</p>

<p>All of our associates are individuals with disabilities looking to gain experience in the science, technology, engineering, or mathematics fields known as the STEM fields. 
    Associates may be paid or unpaid depending on funding. Our associates have the opportunity to work on an internship in our IT services department and/or our research and 
    development department.</p>

<p>All positions are remote, work-from-home positions. All associates must have their own computer, access to the Internet, 
    and be open to using online collaboration tools such as Google Hangouts.</p>

<p>Volunteers are often used to promote the organization and to help with fundraising and with any other needs we may have. Volunteers do not need to necessarily 
have a disability and are unpaid.</p>

<p>As a benefit to our volunteers and associates, we offer <a href="<?= base_url('associates-and-volunteers/employment-services.html') ?>">employment services</a> such as help with resumes, job searches, and job placement.</p>
    
<p>If you are interested in being involved with McGrath Solutions or interested in an internship, please <a href="<?= base_url('associates-and-volunteers/submit-resume.html') ?>">submit your resume</a>. We keep resumes on file,
    and if your skills match what we are looking for, we will contact you. We also encourage you to view our <a href="<?= base_url('associates-and-volunteers/open-positions.html') ?>">open positions</a> listed on our website.</p>