<h1>Associates & Volunteers</h1>

<p>Part of our mission is to provide remote work experience to people with disabilities. We offer internship opportunities 
    to people with disabilities in the information technology and engineering fields. This experience can be used to help expand technical skills and build resumes, 
    which will open the doors to competitive employment. Often times, a barrier to employment is not a lack of education, but a lack of experience.</p>
    
